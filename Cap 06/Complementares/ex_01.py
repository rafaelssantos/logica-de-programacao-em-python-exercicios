# Apenas os operadores de adição e de multiplicação podem ser utilizados com o tipo string.
# O uso dos demais operadores resulta em erro.

# O operador de atribuição realizar uma concatenção de strings
nome1 = "Alan"
nome2 = "Turing"
resultado = nome1 + nome2
print(resultado)

# O operador de multiplicação deve ser usado com um operando int.
# O resultado é uma concatenção repetindo o valor string do outro operando.
nome1 = "John von Neumann"
resultado = nome1 * 3
print(resultado)